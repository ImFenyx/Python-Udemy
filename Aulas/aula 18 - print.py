print(12, 34) #? Um print imprime "argumentos" no terminal;
#* > 12 34
#? Usando (12, 34) dividirá "argumentos".



print(56, 78)
#* > 56, 78


print(56,78,1011, sep="-") #? sep significa "separador", usa-se strings para separar
#* > 56-78-1011

print(56,78,1011, sep="-", end="##") #? end significa o fim do print, quando os argumentos acabam usa-se o end para adicionar uma string de fim
#* > 56-78-1011##

#? Python é case sensitive, então "Print" maíusculo não funciona
