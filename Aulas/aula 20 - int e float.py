# Tipos int e float

#* int => Número inteiro
#? Int representa qualquer número positivo ou negativo natural.
print(11) # int
print(-11) # int
print(0) # int

#* float => Número com ponto flutuante
#? Float representa qualquer número positivo ou negativo decimal.
print(1.1) # float
print(0.0) # float


#? type() mostra o tipo que o Python inferiu ao valor
print(type("Teste")) #* > <class 'str'>
print(type("1")) #* > <class 'int'>

